package com.mall.hangxunc.bean.home;

public class LanguageBean {
    /**
     * name : English
     * code : en-gb
     * href : http://b.hangxunc.com/index.php?route=api/ioslink/getLanguage&amp;language=en-gb
     */

    private String name;
    private String code;
    private String href;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }
}
